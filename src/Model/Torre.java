package Model;

public class Torre {

    private String id, numeroTorre, numeroApts, idProyecto;

    public Torre() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNumeroTorre() {
        return numeroTorre;
    }

    public void setNumeroTorre(String numeroTorre) {
        this.numeroTorre = numeroTorre;
    }

    public String getNumeroApts() {
        return numeroApts;
    }

    public void setNumeroApts(String numeroApts) {
        this.numeroApts = numeroApts;
    }

    public String getIdProyecto() {
        return idProyecto;
    }

    public void setIdProyecto(String idProyecto) {
        this.idProyecto = idProyecto;
    }

    
}
