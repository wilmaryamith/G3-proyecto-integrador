    private void btn_EditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_EditarActionPerformed
        // TODO add your handling code here:
        String cedulaAsesor = txt_BuscarAsesor.getText().trim();

        if (cedulaAsesor.equals("")) {
            JOptionPane.showMessageDialog(null, "Error al tratar de capturar una cedula", "Debes ingresar una cedula", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Asesor actualizarAsesor = new Asesor();
        AsesorController edicionAsesor = new AsesorController();

        _cedula = txt_Cedula.getText();
        _nombre = txt_Nombre.getText();
        _direccion = txt_Direccion.getText();
        _telefono = txt_Telefono.getText();
        _correo = txt_Correo.getText();
        _usuario = txt_Usuario.getText();
        _contrasena = txt_Contrasena.getText();

        actualizarAsesor.setCedula(_cedula);
        actualizarAsesor.setNombre(_nombre);
        actualizarAsesor.setDireccion(_direccion);
        actualizarAsesor.setTelefono(_telefono);
        actualizarAsesor.setCorreo(_correo);
        actualizarAsesor.setUsuario(_usuario);
        actualizarAsesor.setContrasena(_contrasena);

        edicionAsesor.actualizar(actualizarAsesor, cedulaAsesor);
    }//GEN-LAST:event_btn_EditarActionPerformed

