    private void btn_ConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ConsultarActionPerformed
        // TODO add your handling code here:
        txt_BuscarAsesor.setEditable(false);
        String cedulaAsesor = txt_BuscarAsesor.getText();
        if (cedulaAsesor.equals("")) {
            JOptionPane.showMessageDialog(null, "Error al tratar de Buscar", "Debes ingresar una cedula", JOptionPane.ERROR_MESSAGE);
            return;
        }

        AsesorController controladorBuscar = new AsesorController();

        Asesor buscarAsesor = controladorBuscar.lectura(cedulaAsesor);

        if (buscarAsesor != null) {
            txt_Cedula.setText(buscarAsesor.getCedula());
            txt_Nombre.setText(buscarAsesor.getNombre());
            txt_Direccion.setText(buscarAsesor.getDireccion());
            txt_Telefono.setText(buscarAsesor.getTelefono());
            txt_Correo.setText(buscarAsesor.getCorreo());
            txt_Usuario.setText(buscarAsesor.getUsuario());
            txt_Contrasena.setText(buscarAsesor.getContrasena());

        }
    }//GEN-LAST:event_btn_ConsultarActionPerformed

